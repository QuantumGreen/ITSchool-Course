package session3.hw;

// 5. Create a Java program that demonstrates the use of each compound assignment operator with integers.

public class HW5 {
    public static void main(String[] args) {
        int i=42;
        System.out.println(i);
        i += 2;
        System.out.println(i);
        i -= 10;
        System.out.println(i);
        i *= 3;
        System.out.println(i);
        i /= 2;
        System.out.println(i);
        i %= 10;
        System.out.println(i);
    }
}
