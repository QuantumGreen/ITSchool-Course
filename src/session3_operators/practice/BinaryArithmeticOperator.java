package session3_operators.practice;

public class BinaryArithmeticOperator {
    public static void main(String[] args) {
        int number1 = 10;
        int number2 = 20;
        System.out.println("Arighmetic operations with int:");
        System.out.println(number2 + number1);
        System.out.println(number2 - number1);
        System.out.println(number2 * number1);
        System.out.println(number2 / number1);
        System.out.println(number2 % number1);

        double double1 = 19.5;
        double double2 = 4.5;
        System.out.println("Arighmetic operations with double:");
        System.out.println();
        System.out.println(double1 + double2);
        System.out.println(double1 - double2);
        System.out.println(double1 * double2);
        System.out.println(double1 / double2);
        System.out.println(double1 % double2);
    }

}
